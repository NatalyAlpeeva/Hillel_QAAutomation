public class CalcUtil {
    public static double sum(double n, double m) {
        return n+m;
    }
    public static double sub(double n, double m) {
        return n-m;
    }
    public static double mupliplic(double n, double m) {
        return n * m;
    }
    public static double div(double n, double m) {
        return n / m;
    }
    public static double percent(double n, double m) {
        return (n / m)*100;
    }
}
